package homeworkweek6;

/*Program of area and perimeter of rectange*/

public class Program14Perimeter {

    public static void main(String[] args) {


        float w = 5.6f;//Width
        float h = 8.5f;//Height
        float area = w * h;//Area
        float perimeter = 2 * (w + h);//Perimeter

        System.out.print("Area is 5.6 * 8.6 =  ");
        System.out.println(area);
        System.out.print("perimeter is 2*(5.6 + 8.5) =  ");
        System.out.println(perimeter);
    }
}
